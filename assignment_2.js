function gonoVote(array) {
  let haVote = "ha";
  let naVote = "na";
  let haCount = 0;
  let naCount = 0;
  if (!Array.isArray(array)) {
    return "Invalid";
  }
  for (let arr of array) {
    if (arr === haVote) {
      haCount++;
    } else if (arr === naVote) {
      naCount++;
    }
  }
  if (haCount > naCount) {
    return true;
  }else if(haCount === 0 && naCount === 0 ){
    return "Invalid"
  } else if (haCount === naCount) {
    return "equal";
  } else {
    return false;
  }
}

